/** Seedable PRNG (Mulberry32) for deterministic shuffles. */
export function mulberry32(seed: number): () => number {
  return function(): number {
    let t = (seed += 0x6D2B79F5) | 0;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/** Fisherâ€“Yates shuffle (optionally using a seed). */
export function shuffleArray<T>(arr: T[], seed?: number | null): T[] {
  // Define specific type for the random function
  const rand: () => number = seed != null ? mulberry32(seed >>> 0) : Math.random;
  
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}